test("writeDiff materializes a migration folder", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  const users = defineSchema("users", {
    id: column.serial().primaryKey(),
    email: column.text(),
  });

  const result = await writeDiff(db, [users], { dir: workDir, name: "add_users" });
  expect(result.noop).toBe(false);
  expect(result.path).not.toBeNull();
  expect(existsSync(`${result.path}/up.sql`)).toBe(true);
  expect(existsSync(`${result.path}/down.sql`)).toBe(true);

  const up = readFileSync(`${result.path}/up.sql`, "utf-8");
  expect(up).toContain("CREATE TABLE users");
  expect(up).toContain("generated by `migrate diff`");

  await db.close();
});

