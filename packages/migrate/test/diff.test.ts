import { afterEach, beforeEach, expect, test } from "bun:test";
import { existsSync, mkdtempSync, readFileSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import { column, connect, defineSchema } from "../../db/index.ts";
import { plan as diffPlan, writeDiff } from "../diff/index.ts";

let workDir: string;

beforeEach(() => {
  workDir = mkdtempSync(`${tmpdir()}/atlas-diff-`);
});

afterEach(() => {
  rmSync(workDir, { recursive: true, force: true });
});

test("diff against an empty DB plans CREATE TABLE for every schema", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  const users = defineSchema("users", {
    id: column.serial().primaryKey(),
    email: column.text().unique(),
    bio: column.text().nullable(),
  });

  const result = await diffPlan(db, [users]);
  expect(result.ops).toHaveLength(1);
  expect(result.ops[0]?.kind).toBe("create_table");
  expect(result.up).toContain("CREATE TABLE users");
  expect(result.up).toContain("id INTEGER PRIMARY KEY");
  expect(result.up).toContain("email TEXT NOT NULL");
  expect(result.up).toContain("bio TEXT");
  expect(result.up).not.toContain("bio TEXT NOT NULL");
  expect(result.down).toContain("DROP TABLE users");

  await db.close();
});

test("diff detects added columns", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  await db.execute({ text: "CREATE TABLE users (id INTEGER PRIMARY KEY, email TEXT NOT NULL)", values: [] });

  const users = defineSchema("users", {
    id: column.serial().primaryKey(),
    email: column.text(),
    name: column.text().nullable(),
  });

  const result = await diffPlan(db, [users]);
  expect(result.ops).toHaveLength(1);
  expect(result.ops[0]).toMatchObject({ kind: "add_column", table: "users" });
  expect(result.up).toContain("ALTER TABLE users ADD COLUMN name TEXT");
  expect(result.down).toContain("ALTER TABLE users DROP COLUMN name");

  await db.close();
});

test("diff detects dropped columns and dropped tables", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  await db.execute({
    text: "CREATE TABLE users (id INTEGER PRIMARY KEY, email TEXT NOT NULL, deprecated TEXT)",
    values: [],
  });
  await db.execute({ text: "CREATE TABLE legacy (id INTEGER PRIMARY KEY)", values: [] });

  const users = defineSchema("users", {
    id: column.serial().primaryKey(),
    email: column.text(),
  });

  const result = await diffPlan(db, [users]);
  const kinds = result.ops.map((o) => o.kind).sort();
  expect(kinds).toEqual(["drop_column", "drop_table"]);
  expect(result.up).toContain("ALTER TABLE users DROP COLUMN deprecated");
  expect(result.up).toContain("DROP TABLE legacy");

  await db.close();
});

test("diff detects type/nullability mismatches as comments", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  await db.execute({ text: "CREATE TABLE t (id INTEGER PRIMARY KEY, count TEXT)", values: [] });

  const t = defineSchema("t", {
    id: column.serial().primaryKey(),
    count: column.integer(),
  });

  const result = await diffPlan(db, [t]);
  expect(result.ops).toHaveLength(1);
  expect(result.ops[0]?.kind).toBe("alter_column");
  expect(result.up).toContain("-- ALTER t.count");
  expect(result.up).toContain("type text → integer");

  await db.close();
});

