test("writeDiff produces distinct folders on successive runs", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  const usersV1 = defineSchema("users", {
    id: column.serial().primaryKey(),
    email: column.text(),
  });

  const first = await writeDiff(db, [usersV1], { dir: workDir, name: "init" });
  expect(first.noop).toBe(false);
  expect(first.path).not.toBeNull();

  // Apply v1 manually so the live DB now matches usersV1.
  await db.execute({ text: "CREATE TABLE users (id INTEGER PRIMARY KEY, email TEXT NOT NULL)", values: [] });

  // Add a column → second diff should write a new folder.
  const usersV2 = defineSchema("users", {
    id: column.serial().primaryKey(),
    email: column.text(),
    name: column.text().nullable(),
  });
  // Sleep is unnecessary — the timestamp uses seconds-resolution; if the test
  // runs fast enough to share a second, we still want different folder names.
  // Pass a different `name` to guarantee uniqueness regardless of timing.
  const second = await writeDiff(db, [usersV2], { dir: workDir, name: "add_name" });
  expect(second.noop).toBe(false);
  expect(second.path).not.toBe(first.path);
  expect(existsSync(`${second.path}/up.sql`)).toBe(true);

  await db.close();
});

test("nullable columns without explicit defaults don't trigger spurious diffs", async () => {
  // Live: column.text() with no DEFAULT; schema: same. Should be a no-op even
  // though one path tracks `hasDefault: false` and the other might not.
  const db = connect({ driver: "sqlite", path: ":memory:" });
  await db.execute({
    text: "CREATE TABLE t (id INTEGER PRIMARY KEY, note TEXT)",
    values: [],
  });

  const t = defineSchema("t", {
    id: column.serial().primaryKey(),
    note: column.text().nullable(),
  });

  const result = await diffPlan(db, [t]);
  expect(result.ops).toEqual([]);

  await db.close();
});

test("internal tables (schema_migrations, sqlite_*) are ignored", async () => {
  const db = connect({ driver: "sqlite", path: ":memory:" });
  await db.execute({
    text: "CREATE TABLE schema_migrations (name TEXT PRIMARY KEY, applied_at TIMESTAMP)",
    values: [],
  });
  await db.execute({ text: "CREATE TABLE users (id INTEGER PRIMARY KEY)", values: [] });

  const users = defineSchema("users", { id: column.serial().primaryKey() });
  const result = await diffPlan(db, [users]);
  expect(result.ops).toEqual([]);

  await db.close();
});
